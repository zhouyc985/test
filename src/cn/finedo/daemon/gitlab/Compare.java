package cn.finedo.daemon.gitlab;

import java.util.ArrayList;
import java.util.List;



import cn.finedo.common.domain.BaseDomain;

public class Compare  extends BaseDomain{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Commit commit;
	private List<GitProjectInfo> commits=new ArrayList<GitProjectInfo>();
	private List<Diff> diffs=new ArrayList<Diff>();
	private boolean compare_timeout;
	private boolean compare_same_ref;
	
	public Commit getCommit() {
		return commit;
	}
	public void setCommit(Commit commit) {
		this.commit = commit;
	}
	public List<GitProjectInfo> getCommits() {
		return commits;
	}
	public void setCommits(List<GitProjectInfo> commits) {
		this.commits = commits;
	}
	public List<Diff> getDiffs() {
		return diffs;
	}
	public void setDiffs(List<Diff> diffs) {
		this.diffs = diffs;
	}
	public boolean isCompare_timeout() {
		return compare_timeout;
	}
	public void setCompare_timeout(boolean compare_timeout) {
		this.compare_timeout = compare_timeout;
	}
	public boolean isCompare_same_ref() {
		return compare_same_ref;
	}
	public void setCompare_same_ref(boolean compare_same_ref) {
		this.compare_same_ref = compare_same_ref;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
