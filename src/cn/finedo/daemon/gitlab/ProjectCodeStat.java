package cn.finedo.daemon.gitlab;

import java.util.ArrayList;
import java.util.List;

public class ProjectCodeStat {
	
	private String statid;// id 
	private String revision;//版本号
	private String projectid;//项目id
	private String codetype;//代码类型
	private String opttime;//提交时间
	private String userid;//提交人编号
	private String addcount;//增加行数
	private String removecount;//删除行数
	private String modifycount;//修改行数
	private String tool;
	private String address;
	
	List<GitProjectInfo> diffcommit = new ArrayList<GitProjectInfo>();
	List<Change> listchange = new ArrayList<Change>();
	
	GitProjectInfo gitinfo=new GitProjectInfo();
	
	
	public GitProjectInfo getGitinfo() {
		return gitinfo;
	}
	public void setGitinfo(GitProjectInfo gitinfo) {
		this.gitinfo = gitinfo;
	}
	public List<Change> getListchange() {
		return listchange;
	}
	public void setListchange(List<Change> listchange) {
		this.listchange = listchange;
	}
	public List<GitProjectInfo> getDiffcommit() {
		return diffcommit;
	}
	public void setDiffcommit(List<GitProjectInfo> diffcommit) {
		this.diffcommit = diffcommit;
	}
	public String getTool() {
		return tool;
	}
	public void setTool(String tool) {
		this.tool = tool;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getStatid() {
		return statid;
	}
	public void setStatid(String statid) {
		this.statid = statid;
	}
	public String getRevision() {
		return revision;
	}
	public void setRevision(String revision) {
		this.revision = revision;
	}
	public String getProjectid() {
		return projectid;
	}
	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}
	public String getCodetype() {
		return codetype;
	}
	public void setCodetype(String codetype) {
		this.codetype = codetype;
	}
	public String getOpttime() {
		return opttime;
	}
	public void setOpttime(String opttime) {
		this.opttime = opttime;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getAddcount() {
		return addcount;
	}
	public void setAddcount(String addcount) {
		this.addcount = addcount;
	}
	public String getRemovecount() {
		return removecount;
	}
	public void setRemovecount(String removecount) {
		this.removecount = removecount;
	}
	public String getModifycount() {
		return modifycount;
	}
	public void setModifycount(String modifycount) {
		this.modifycount = modifycount;
	}
	
}
