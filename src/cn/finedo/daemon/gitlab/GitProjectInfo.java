package cn.finedo.daemon.gitlab;


import cn.finedo.common.domain.BaseDomain;

public class GitProjectInfo extends BaseDomain{
	private static final long serialVersionUID = 1L;

	private String id;
	
	private String description;
	
	private String name;
	
	private String web_url;
	
	private String path;
	
	private String created_at;
	
	private String last_activity_at;

	private String short_id;
	
	private String author_name;
	
	private String ssh_url_to_repo;
	private String author_email;
	private String username;
	
	private String email;
	private String committer_email;
	private String message;
	
	
	
	
	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getCommitter_email() {
		return committer_email;
	}

	public void setCommitter_email(String committer_email) {
		this.committer_email = committer_email;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getAuthor_email() {
		return author_email;
	}

	public void setAuthor_email(String author_email) {
		this.author_email = author_email;
	}

	public String getSsh_url_to_repo() {
		return ssh_url_to_repo;
	}

	public void setSsh_url_to_repo(String ssh_url_to_repo) {
		this.ssh_url_to_repo = ssh_url_to_repo;
	}

	public String getAuthor_name() {
		return author_name;
	}

	public void setAuthor_name(String author_name) {
		this.author_name = author_name;
	}

	public String getShort_id() {
		return short_id;
	}

	public void setShort_id(String short_id) {
		this.short_id = short_id;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWeb_url() {
		return web_url;
	}

	public void setWeb_url(String web_url) {
		this.web_url = web_url;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}



	

	@Override
	public String toString() {
		return "GitProjectInfo [id=" + id + ", description=" + description
				+ ", name=" + name + ", web_url=" + web_url + ", path=" + path
				+ ", created_at=" + created_at + ", last_activity_at="
				+ last_activity_at + ", short_id=" + short_id
				+ ", author_name=" + author_name + ", ssh_url_to_repo="
				+ ssh_url_to_repo + ", author_email=" + author_email
				+ ", username=" + username + ", email=" + email
				+ ", committer_email=" + committer_email + "]";
	}

	public String getCreated_at() {
		return created_at;
	}

	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}

	public String getLast_activity_at() {
		return last_activity_at;
	}

	public void setLast_activity_at(String last_activity_at) {
		this.last_activity_at = last_activity_at;
	}
}
